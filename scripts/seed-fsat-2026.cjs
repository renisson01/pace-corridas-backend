async function main() {
  console.log('🗑️  Deletando todas as corridas existentes...');
  
  const deleted = await prisma.race.deleteMany({});
  console.log(`✅ ${deleted.count} corridas deletadas.`);

  console.log(`\n📥 Inserindo ${corridas.length} corridas da FSAt 2026...`);

  let inseridas = 0;
  let erros = 0;

  for (const corrida of corridas) {
    try {
      await prisma.race.create({
        data: {
          name: corrida.name,
          city: corrida.city,
          state: corrida.state,
          date: corrida.date,
          distances: corrida.distances,
          arena: corrida.arena || null,
          organizer: corrida.organizer || null,
          organizerContact: corrida.organizerContact || null,
          phone: corrida.phone || null,
          registrationUrl: corrida.registrationUrl || null,
          permit: corrida.permit || null,
          status: corrida.status || 'upcoming',
          imageUrl: null,
          description: null,
        }
      });
      inseridas++;
      process.stdout.write(`\r  ✅ ${inseridas}/${corridas.length} inseridas`);
    } catch (err) {
      erros++;
      console.error(`\n❌ Erro em "${corrida.name}":`, err.message);
    }
  }

  console.log(`\n\n🎉 CONCLUÍDO!`);
  console.log(`   ✅ ${inseridas} corridas inseridas`);
  console.log(`   ❌ ${erros} erros`);
  console.log(`\n🔗 Verifique: https://web-production-990e7.up.railway.app`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
